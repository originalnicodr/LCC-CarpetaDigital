#include <stdio.h>
#include <assert.h>

/** Imprimir en binario. */

void binaryPrint(int valor){
  for(int i=31; i>=0; i--){
    if((i+1)%8 == 0){
      printf(" ");
      }
      (valor&(1<<i)) ? printf("1"):printf("0");
    }
  printf(" =  %d\n", valor);
}


